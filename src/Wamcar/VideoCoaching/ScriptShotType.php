<?php


namespace Wamcar\VideoCoaching;


class ScriptShotType
{

    /** @var int */
    private $id;
    /** @var string */
    private $label;

    /**
     * @return null|int
     */
    public function getId(): ?int
    {
        return $this->id;
    }

    /**
     * @return null|string
     */
    public function getLabel(): ?string
    {
        return $this->label;
    }

    /**
     * @param string $label
     */
    public function setLabel(string $label): void
    {
        $this->label = $label;
    }
}